package game

import "strings"

type Preset struct {
	Name     string
	Id       int
	worldGen func() *World
}

var (
	presets            []Preset
	presetCircleOfFire Preset
	presetPulsar       Preset
	presetSpider       Preset
	presetGlider       Preset
)

func init() {
	presetCircleOfFire = Preset{
		Name:     "fire",
		Id:       -1,
		worldGen: circleOfFire(),
	}
	presetSpider = Preset{
		Name:     "spider",
		Id:       -2,
		worldGen: spider(),
	}
	presetPulsar = Preset{
		Name:     "pulsar",
		Id:       -3,
		worldGen: pulsar(),
	}
	presetGlider = Preset{
		Name:     "glider",
		Id:       -4,
		worldGen: glider(),
	}
	presets = append(presets, presetCircleOfFire, presetPulsar, presetSpider, presetGlider)
}

func AvailablePresets() []string {
	var presetNames []string
	for _, preset := range presets {
		presetNames = append(presetNames, preset.Name)
	}
	return presetNames
}

func FindPreset(name string) *Preset {
	for _, preset := range presets {
		if strings.EqualFold(preset.Name, name) {
			return &preset
		}
	}
	return nil
}

func GenPreset(presetId int) (w *World) {
	for _, preset := range presets {
		if preset.Id == presetId {
			return preset.worldGen()
		}
	}
	return nil
}

